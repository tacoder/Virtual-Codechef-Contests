<?php require("../../includes/header.php"); ?><h1>Upside Down</h1><div class="content">

<p>Amy is a bright kid. She recently learnt the numbers from 0 to 9, and spends all her time these days, trying to write larger numbers. One day she wrote a number on a paper, and turned it upside down. It surprised and amazed her that the writing on the paper still made sense.</p>
<p>So, now she has created a game for herself, where she writes numbers and looks at them upside down to see if it still is valid. Help Amy in this game.</p>
<h3>Input</h3>
<p>For each of t testcases,<br />
Given a number n, find if it is valid while being viewed upside down.</p>
<h3>Output</h3>
<p>If the number is valid print YES followed by the new number in the next line. Else print NO.</p>
<h3>Constraints</h3>
<p>1&lt;=n&lt;=10^50</p>
<p>1&lt;=t&lt;=1000</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
