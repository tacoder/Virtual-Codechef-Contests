<?php require("../../includes/header.php"); ?><h1>King of the Ring</h1><div class="content">

<p>Children of New Bernsburg love to play creative games. Once upon a time they decided to play a game called, "KING OF THE RING".</p>
<p>
In this game they form a ring, a random child is selected and they start counting clockwise from that child until a fixed number (n, which is given at the starting of the game) is reached. When the count reaches n, the child on the nth spot is eliminated. The game continues again starting with the next child and the process continues until a single child remains.</p>
<p>
Your aim is to print the position of the child which remains till last and hold the title King of the Ring.</p>
<h3>Input</h3>
<p>The first line of input contains number of children, m and the second line contains the fixed number, n, the factor by which children are eliminated.</p>
<h3>Output</h3>
<p>The output must print the position of the last child staying in the game.</p>
<h3>Example</h3>
<pre>
<b>Input:</b>

10
6

<b>Output:</b>

3

