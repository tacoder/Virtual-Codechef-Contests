<?php require("../../includes/header.php"); ?><h1>Expected Distance</h1><div class="content">
<p> </p>
<p>There are two circles of radius <b>R</b> on a coordinate system such that the center of first circle lies on <b>(-R,0)</b> and the center of second circle lies on <b>(R,0)</b>. There exist two points on the coordinate system such that first point lies on the center of one circle and the other point can lie anywhere on the circumference of another circle with equal probability everywhere.<br />
Find out the <a href="http://en.wikipedia.org/wiki/Expected_value">expected value</a> of the distance between both the points.</p>
<p> </p>
<h3>Input</h3>
<p>Contains a single integer <b>R</b> - the radius of both circles.</p>
<p> </p>
<h3>Output</h3>
<p>Output a the integer part of the expected value of distance between those points.<br /><br />
The decimal part of the answer has to be dropped.</p>
<p> </p>
<h3>Constraints</h3>
<p><b>0</b> ≤ <b>R</b> ≤ <b>10^5</b> </p>
<p> </p>
<h3>Example</h3>
<pre><b>Input:</b>
1

<b>Output:</b>
2
