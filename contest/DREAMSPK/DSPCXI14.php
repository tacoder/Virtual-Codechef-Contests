<p>In one of our Paritraan trips, Akhil Ranjan and Amritesh Kumar were trying to teach the BODMAS rule to children. Now after they taught the children they want to conduct an online test. You have to help them. The children have been supplied with a set of mathematical equations. These equations do not contain brackets. The </p>
<p>task is to place the brackets in such a way that the numerical value obtained from the expression is maximum.</p>
<p>Problem Setters: <a href= "http://www.facebook.com/Aasthik">Vishal Gupta</a> and <a href= "http://www.facebook.com/zeeshan.zahid1">Zeeshan Zahid</a></p>
<h3>Input</h3>
<p>The first line of input contains the number of testcases T ( T&lt; 5000 ). Each of the following T lines consists of an arithemtic expression. This expression consists </p>
<p>of numbers (0-9) seperated by one of the two operators '*' and '+'. There are no spaces between the characters. Each line contains less than 500 characters.</p>
<h3>Output</h3>
<p>For each expression output the maximum numerial value possible. The results of the calculations are smaller than 2^64.</p>
