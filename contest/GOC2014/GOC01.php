<h3>Problem Description</h3>
<p>Chef Boogy was very pleased with the delicacies cooked by his subordinate chefs. In order to judge the individual cooking skills of his chefs, he decided to conduct a cook off championship for all subordinate chefs. N teams of chefs are decided to participate in cook off and a knockout fixture is set. Each team consists of only one chef. In case the number of chef teams are less than 2^b (for any b) then blank teams are added to make it equal 2^b.Calculate the minimum number of cook offs to be played in the championship. A match between a chef team and blank team or a match between two blank teams is not counted.</p>
