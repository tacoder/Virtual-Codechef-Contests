<p>Each line of Testcase ( approx. 10 ^ 6) will give the number of teams(0 &lt; N &lt; 10 ^ 9) to participate. Input will terminate when there is no further input. </p>
<h3>Output</h3>
<p>Print Minimum Number of Matches to be played for each given testcase </p>
<h3>Constraints</h3>
<p>Should contain all the constraints on the input data that you may have. Format it like:</p>
<ul>
<li><b>1</b> ≤ <b>T</b> ≤ <b>10^6</b></li>
<li><b>0</b> &lt; <b>N</b> &lt; <b>10^9</b></li>
</ul>
