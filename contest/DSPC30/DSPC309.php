<p class="MsoNormal" align="center"><b><u><span>TRIPLE ARITHMETIC</span></u></b></p>
<p class="MsoNormal"><span>The CGBSE<br />
Board has decided to introduce TRIPLES as a part of their Curriculum of High<br />
School. They believe this will make it easier for students to learn Co-ordinate<br />
Geometry and Calculus easily.</span></p>
<p class="MsoNormal"><span>Any Triple<br />
of the form <span class="SpellE">a<span class="GramE">,b,c</span></span> represents<br />
a right angled triangle with Base = a; Height = b; Hypotenuse = C. Hence any<br />
triple represents an angle A such that <span class="SpellE">cosA</span> = a/c.</span></p>
<p class="MsoNormal"><span>A Triple is called<br />
a Prime Triple if its elements are whole number and have no common factor. For example<br />
(3, 4, 5) and (24, 7, 25) are Prime Triples. However, (6, 8 10) is not because<br />
2 is a common Factor.</span></p>
<p class="MsoNormal"><span>The very<br />
basic Triple Arithmetic includes addition of Angles of Triple, i.e. if a1,a2,a3<br />
define angle A and b1,b2,b3 define angle B then you need to find <b>Prime Triple</b> corresponding to angles<br />
A+B or A-B.</span></p>
<p class="MsoNormal"><span>©Vishal<br />
Gupta</span></p>
<p class="MsoNormal"><b><u><span>Input:</span></u></b></p>
<p class="MsoNormal"><span>Input contains a number of test<br />
cases. Each test case contains 3 lines.<b><u></u></b></span></p>
<p class="MsoNormal"><span>First Line contains either ‘+’ or<br />
‘-‘.</span></p>
<p class="MsoNormal"><span>Second Line contains Triple for angle<br />
A a1<span class="GramE">,a2,a3</span></span></p>
<p class="MsoNormal"><span>Third Line contains Triple for Angle<br />
B b1<span class="GramE">,b2,b3</span></span></p>
<p class="MsoNormal"><span> </span></p>
<p class="MsoNormal"><b><u><span>Output:</span></u></b></p>
<p class="MsoNormal"><span class="GramE"><span>If the input is +,<br />
you need to output the Prime Triple Corresponding to angle A+B.</span></span><span></span></p>
<p class="MsoNormal"><span class="GramE"><span>If the input is -,<br />
you need to output the Prime Triple Corresponding to angle A-B.</span></span><span></span></p>
<p class="MsoNormal"><span> </span></p>
<p class="MsoNormal"><b><u><span>Input:</span></u></b></p>
<p class="MsoNormal"><span>+<br />4 3 5<br />15 8 17<br />-<br />4 3 5<br />3 4 5<br />+<br />3 4 5<br />3 4 5<br />+<br />-1 0 1<br />0 1 1</span></p>
<p class="MsoNormal"><b><u><span>Sample Output:</span></u></b><span></span></p>
<p class="MsoNormal"><span>36 77 85<br />24 -7 25<br />-7 24 25<br />0 -1 1</span></p>
