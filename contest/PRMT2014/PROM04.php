<h2>Problem Statement  </h2>
<p>A mathematics professor uploads all necessary course material on his site. But he wanted his students to think and apply their minds. So he conceived an idea to get all of his students involved and share in his love for numbers. Every time they would access his site they would be given 10 sets of numbers from which they would have to find the longest increasing subsequences in each set. The answer for each would be the numbers appearing in the respective subsequence (if for one set there were more than one subsequence then the answer would be the union of all numbers appearing in the subsequences). Help the students solve the problems so that they can access the site.
</p><p> </p>
<h3>Input</h3>
<p>Ten test cases (given one under another, you have to process all!). Each test case consists of two lines. In the first line there is a number n (1&lt;=n&lt;=100000). In the second line: an n-element permutation - n numbers separated by single spaces.
</p>
<h3>Output</h3>
<p> For every test case your program should write two lines. In the first line - the number of numbers appearing in your answer from the input permutation. In the second line the numbers separated by single spaces in increasing order.
</p>
<h3>Example</h3>
<pre><b>Input:</b>
5
2 1 4 5 3
(9 other test cases)

<b>Output:</b>
4
1 2 4 5
(9 other test  cases)

</pre><p>
