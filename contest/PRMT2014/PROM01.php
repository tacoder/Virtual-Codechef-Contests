<?php require("../../includes/header.php"); ?><h1>Balloon Fight</h1><div class="content">
<h2>Problem Statement  </h2>
<p>Rohit and Ali are having a water balloon fight on their terrace. Their terrace can be thought of as a grid playing field. In order for any of them to win, one of them needs to get a direct hit and they need to be in a direct line of vision of the other person, horizontal, vertical or diagonal. Given a (P*Q) playing field you will have to decide in how many ways can the two of them be positioned to obtain a head-on attack on each other.</p>
<p> </p>
<h3>Input</h3>
<p>Input file can contain up to 5000 lines of inputs. Each line contains two positive integers which denote the value of P and Q (0&lt; P, Q&lt;10^6) respectively. Input is terminated by a line containing two zeroes. These two zeroes need not be processed.</p>
<h3>Output</h3>
<p> For each line of input produce one line of output, denoting the number of ways they can be in an attacking position in the (P*Q) playing field. All output values will fit in 64-bit signed integer.
</p>
<h3>Example</h3>
<pre><b>Input:</b>
2 2
100 223
0 0

<b>Output:</b>
12
10907100

</pre><p>
