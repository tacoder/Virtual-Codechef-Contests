<h2>Problem Statement  </h2>
<p>Mark and his classmates were playing a game called “last man standing”. The last person left would have to be treated by the rest.  The game went like this. From among n of his classmates, numbered 1, 2,...,n, standing in a circle every mth person is going to be disqualified.  Mark is smart enough to choose the position of the last remaining person, thus always getting a treat. Now to make the game more fun he wanted his friends to last longer than the rest of his classmates. In the circle the first p are his good friends and the last p, his other classmates. You have to determine a minimal m such that all his other classmates will be out of the game before the first friend.</p>
<p> </p>
<h3>Input</h3>
<p>The first line contains t, the number of test cases (1&lt;=t&lt;=100). The following t lines have a positive integer p (1&lt;=p&lt;14) </p>
<h3>Output</h3>
<p>The output file will consist of t lines, each containing the integer m corresponding to p in the input file. </p>
<h3>Example</h3>
<pre><b>Input:</b>
2
3
4

<b>Output:</b>
5
30
</pre><p> </p>
