<h2>Problem Statement  </h2>
<p>	Darian has a very poor memory. He can never remember any password he sets for his laptop which gets him into a lot of trouble. But the one thing he had, to save him was that he was good with numbers. He could calculate anything but then again he had a problem remembering a string of numbers. So he devised a way of not remembering a password but at the same time keeping his laptop secure. By this method, his password contains 1s and 0s. It does not contain any leading 0. There are no consecutive 1s. This list of numbers would be 1, 10, 100, 101, 1000 and so on. So every time he switched on his computer he would get a different P for which he has to calculate the Pth number in the list.</p>
<p> </p>
<h3>Input</h3>
<p>The first line of the input contains one integer T (1&lt;=T&lt;=100) the number of test cases. Each test case contains one integer P. where 1&lt;=P&lt;=10^9<br />
</p><p p>
<h3>Output</h3>
</p><p>For each test case output one line containing the pth number in the list.</p>
<h3>Example</h3>
<pre><b>Input:</b>
2 
10
30

<b>Output:</b>
10010
1010001

</pre><p> </p>
