<p>A positive rational number is a real number which can be expressed as P/Q where P, Q are coprime positive intergers. The Chef is very hungry and can get food only if he solves the following question. "Given a number N, find the number of positive rational numbers less than 1 such that when expressed as P/Q,  both P, Q are less than or equal to N". Please help the Chef in answering this question. The Chef promises to share the food with you.</p>
<p><br /></p>
<h3>Input</h3>
<p>The first line contain a single integer T, the number of test cases. Following T (&lt;= 1000) lines will contain a test case, a single number N (&lt;= 1000000).</p>
<p><br /></p>
<h3>Output</h3>
<p>For each test case print the output on a single line.</p>
<p><br /></p>
<h3>Example</h3>
<pre>
<b>Input:</b>
