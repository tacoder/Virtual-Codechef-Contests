<p>The New Zealand government is super-excited about the Cricket World Cup 2015 happening in their country. Hence, it has decided to conduct a census on cricket to find out how many people are interested in the sport, etc.</p>
<p>The statistics obtained are given to us as a range of numbers from A to B (inclusive). In these statistics, the government is interested in some particular numbers called "Kiwi Numbers".</p>
<p>A number is called a Kiwi Number if it has a prime number of distinct factors. A prime number N has exactly two divisors, 1 and N.</p>
<p>Can you help the government by calculating the number of Kiwi numbers in the given range? </p>
<p><strong>Input:</strong><br /> The first line contains the number of test cases T.<br /> Each of the next T lines contains two space separated numbers A and B.</p>
<p><strong>Output:</strong><br /> For each test case, output the required answer on a separate line.</p>
<p><strong>Constaints:</strong><br /> 1 &lt;= T &lt;= 100<br /> 2 &lt;= A &lt;= B &lt;= 1000000000<br /> B - A &lt;= 200000</p>
<p><strong>Sample Input:</strong><br /> 3<br /> 2 10<br /> 100 100<br /> 578 720</p>
<p><strong>Sample Output:</strong><br /> 6<br /> 0<br /> 23</p>
<p><strong>Explanation:</strong><br /> For the first case, the Kiwi numbers are 2, 3, 4, 5, 7, 9.</p>
