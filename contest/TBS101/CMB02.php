<p>The first line contains integer t, the number of test cases. </p>
<p>Integers K are given in the next t lines.</p>
<h3>Output</h3>
<p>For each K, output the smallest palindrome larger than K.</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
1
808

