<?php require("../../includes/header.php"); ?><h1>Paths to a point</h1><div class="content">

<p>In a 2D co-ordinate system, A is situated at origin and he wants to move from origin to a point, he can move only parallel to X-axis or Y-axis . Once he takes a forward step he can't take backward. Your job is to find out number of ways to go from origin to a point.</p>
<h3>Input</h3>
<p>Input consists of two integral values(&lt;50) indicating the x and y co-ordinates of a point with a space between them.</p>
<h3>Output</h3>
<p>Output consists of number of ways A can move from origin to a given point as specified.</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
40 0


<b>Output:</b>
1
