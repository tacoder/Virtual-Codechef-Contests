The second line contains all the <b>N</b> elements Pintel gave to Ragetti. <b>(n<sub>1</sub>, n<sub>2</sub>, n<sub>3</sub> ...)</b></p>
<h3>Output</h3>
<p>The <b>T</b> lines of output would contain the maximum sum that could be created by Ragetti for each test case.</p>
<h3>Constraints</h3>
<ul>
<li><b>1</b> &lt;= <b>T</b> &lt;= <b>10</b></li>
<li><b>1</b> &lt;= <b>N</b> &lt;= <b>100000</b></li>
