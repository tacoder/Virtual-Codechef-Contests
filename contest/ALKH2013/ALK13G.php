<p>Pintel is a pirate and former Black Pearl crew member under Captain Barbossa. He was imprisoned after the Aztec curse was broken, but somehow escaped from there. Ragetti is Pintel's inseparable crew mate. He has a wooden eye, and despite being illiterate, has begun "reading" the Bible, with the excuse that "you get credit for trying."</p>
<p>
Pintel and Ragetti are best friends, but their opinions differ in every thing. They like giving challenges to each other and to prove the other one wrong. One day Pintel gave Ragetti <b>N</b> numbers. Ragetti is highly optimistic so he likes positive numbers but Pintel, on the opposite, likes negative numbers. After getting the <b>N</b> numbers Ragetti can multiply any range <b>[L, R] (1 &lt;= L &lt;= R &lt;= N)</b> by the number <b>-1</b> atmost once so that sum of all the <b>N</b> numbers is maximum after the above operation is applied. Ragetti is weak in maths and programming. So, he needs your help as you are the only programmer among the crew members.
