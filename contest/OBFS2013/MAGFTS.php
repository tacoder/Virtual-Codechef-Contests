<p> </p>
<p>On Sheldon's birthday bash, he received n number of Magic Gifts in a hotel. Being crazy and very particular about the things, he doesn't want to open them unless they are transferred to his home. Since the Magic Gifts can be very large in number, it is impossible to transfer them to his place manually one by one. Therefore, he seeks your help to perform a Magic-T one or more times to transfer them. </p>
<p>Now, the Magic-T behaves very differently. If n, number of Magic Gifts, is not a prime number, they can be transferred to Sheldon's home using Magic-T at once. Otherwise the gifts will disappear instead of getting transferred.  He doesn't want to lose any of his gifts at any cost so he asks you to transfer them using Magic-T as few times as possible. </p>
<p> </p>
<h3>Input</h3>
<p>The first line of input contains an integer T, the number of test cases. Then T test cases follow.</p>
<p>Each test case consists of a line containing a number n, number of Magic Gifts.</p>
<h3>Output</h3>
<p>Output on a new line the minimum number of times you will have to use Magic-T to transfer n Magic Gifts.</p>
<h3>Constraints</h3>
<p>1 &lt;= T &lt;= 10^4<br/><br />
1 &lt;= n &lt;= 10^12</br/></p>
<p> </p>
<h3>Example</h3>
<pre><b>Input:</b>
2
4
5

<b>Output:</b>
1
2
