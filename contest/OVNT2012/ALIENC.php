<?php require("../../includes/header.php"); ?><h1>Chocolates and Aliens</h1><div class="content">

<p>
An alien from a galaxy far far away has come to Earth to check out the best commodity on Earth - chocolate. The alien, on 29th January, 2013 has <b>N</b> chocolates.<br />
On each day, if the number of chocolates is even, he will eat half of the chocolates. If it is odd, he brings more chocolates such that the new number of chocolates is thrice the current number of chocolates plus three. If the number of chocolates left is 1 on any day, then the alien will teleport back to his planet.<br />
Will the alien still be on Earth on 29th January 2113?</p>
<h3>Input</h3>
<p>
A integer T(1 ≤ T ≤ 10<sup>5</sup>) containing number of test cases followed by T integers each containing a integer N (1 ≤ N ≤ 10<sup>18</sup>), number of chocolates.</p>
<h3>Output</h3>
<p>
Output T lines, each line containing "Yes" or "No" whether he will be on earth or not after the above given time.</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
2
2
5

<b>Output:</b>
No
Yes
