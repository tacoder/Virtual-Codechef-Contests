<h3>Problem description.</h3>
<p>Liza wants to share her  pizza among n friends, What are the minimum number of straight knife strokes required to cut the pizza into n pieces?
 </p>
<h3>Input</h3>
<p>The first line contains number of test cases <b>T</b>.<br />
The next <b>T</b> lines contain the input numbers <b>N</b>, the numbers of pieces required.
</p>
<h3>Output</h3>
<p>For each test case, output in a new line, the minimum number of straight knife strokes(integer), required for the test case.
</p>
<h3>Constraints</h3>
<ul>
<li><b>1</b> ≤ <b>t</b> ≤ <b>1000</b></li>
<li><b>1</b> ≤ <b>n</b> ≤ <b>1000</b></li>
</ul>
<p> </p>
