number; for example, 155349.WAP to check whether a number is bouncy or not.</p>
<h3>Input</h3>
<p>The input consists of one number given as input at a time,the program checks if the number is bouncy or not.The number of test cases have to be specified in the first input.</p>
<h3>Output</h3>

<p>The output displays whether the number is bouncy or not,i.e it displays 1 when the number is bouncy and 0 when its not.</p>
