<h3>Background</h3>
<p>
Given a natural number n, find the summation of all its proper divisors.<br />
Definition: A proper divisor of a natural number is the divisor that is strictly less than the number. e.g. number 20 has 5 proper divisors: 1, 2, 4, 5, 10, and the divisor summation is: 1 + 2 + 4 + 5 + 10 = 22.
</p>
<h3>Input: </h3>
<p>
An integer stating the number of test cases T and that many lines follow, each containing natural number n for which output will be summation of all its proper divisors.</p>
<h3>Output: </h3>
<p>
One integer each line: the divisor summation of the integer given respectively.
</p>
<h3>Constraints</h3>
<p>
1&lt;=T &lt;=200000<br />
1 &lt;= n &lt;= 500000
</p>
<h3>Sample Input</h3>
<p><pre>
3
2
10
20
</pre></p>
<h3>Sample Output</h3>
<p><pre>
1
8
22
</pre></p>
