<?php require("../../includes/header.php"); ?><h1>Witua and Math</h1><div class="content">

<p>Witua is a little student from the University of Lviv. He enjoys studying math. Witua knows a lot of famous mathematicians like Eratosthenes, Pythagoras, Fermat, Diophantus, Furko, Gauss and so on. However, his favorite one is Euler. The only thing Witua likes more than Euler is <a href="http://en.wikipedia.org/wiki/Euler%27s_totient_function">Euler’s totient function</a> <b>φ</b>. He is exploring the nature of this function. One of the steps of his work is finding φ(i)/i for all 2≤i≤<b>N</b>. He doesn’t need to know every such value, but Witua wonders for what value i, is φ(i)/i the maximum he can get? Help little student to find such i that φ(i)/i is maximum among all the  2≤i≤<b>N</b>.</p>
<h3>Input</h3>
<p>The first line contains single integer <b>T</b> - the number of test cases. Each of the next T lines contains a single integer <b>N</b>.</p>
<h3>Output</h3>
<p>For every test case output i such that φ(i)/i is maximum among all i (2≤i≤<b>N</b>) in a separate line.</p>
<h3>Constrains</h3>
<p><b>T</b> (1≤<b>T</b>≤500 )</p>
<p><b>N</b>(2≤<b>N</b>≤10^18)</p>
<h3>Example</h3>
<pre><b>Input:</b>
3
2
3
4

<b>Output:</b>
2
3
3

<b>Explanation</b>
<b>φ(2)/2=1/2
φ(3)/3=2/3
φ(4)/4=2/4</b>
