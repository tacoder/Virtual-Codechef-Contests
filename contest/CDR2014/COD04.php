<h2>Rohan and Digits</h2>
<p>Rohan is very fond of digits (0-9).<br />
He has a maths project at school and was asked to bring something cool. So he thought of cutting out digits in a piece of chart paper and coloring<br />
them with different colors.While he was doing this, he noticed that, for digit 0, he had one hole in the chart paper. A hole can be of any shape.<br />
So, 0,4,6,9 all has one hole. 8 has two holes.<br />
Rest of the digits don't have holes. Rohan was thinking of writing some computer program to enhance his programming skill. But he needs to verify his solution. Help him. Now, as a programmer what you have to do is pretty simple. Given a specific number of holes x, you have to print the<br />
smallest integer having x holes in it. Note that this number should not have any leading zeros.</p>
<h3>INPUT</h3>
<p>The first line contains an integer x, which represents number of holes</p>
<h3>CONTRAINTS : </h3>
<ul>
<li> <b>0</b> &lt;= <b>x</b> &lt;= <b>510</b> </li>
</ul>
<h3>OUTPUT : </h3>
<p>For each test case, print the smallest integer containing exactly x holes in it. Note: Output can be very very long.</p>
