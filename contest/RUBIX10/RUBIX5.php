<p>
A lucky number N is defined as a number which has <b>only two</b> factors, both of them <b>distinct and prime</b> and the absolute difference between the two factors is not more than 50.</p>
<p>Given two integers, a and b, find the total lucky numbers in the range a to b (inclusive).</p>
<h3>Input</h3>
<p>1.The first input will be T, the number of test-cases. T &lt;= 2000. T lines follow, one for each test case. <br /><br />
2.For each test case there are two integers a and b on each line. 1 &lt;= a &lt;= b &lt;= 100000 <br /></p>
<h3>Output</h3>
<p>
For every test case, print the number of lucky numbers from a to b (inclusive).<br /></p>
<h3>Example</h3>
<pre>
<b>Input:</b>
1
1 20
<b><br />Output:</b>
4
<b><br />Explanation</b>
From 1 to 20, there are four lucky numbers, 6,10, 14, and 15. 
