<?php require("../../includes/header.php"); ?><h1>Jerry and Fibonacci</h1><div class="content">
<p>Today, as usual, Jerry comes up with a new puzzle for Tom. Jerry knows very well that Tom is well aware of the concepts of fibonacci series. So he intends to present the problem with a twist. By choosing any random integer Jerry asks Tom to tell whether the given number is a fibonacci number.</p>
<p> </p>
<h3>Input</h3>
<ul>
<li>The first line of the input contains an integer <b>T</b> denoting the number of test cases.</li>
<li>Next T lines which contain a random number <b>N</b> chosen by Jerry.</li>
</ul>
<p> </p>
<h3>Output</h3>
<ul>
<li>For each input random integer you need to tell if the integer is fibo number or not. If the integer is fibo number output "<b>is fibo</b>" otherwise output "<b>not fibo</b>".</li>
</ul>
<p> </p>
<h3>Constraints</h3>
<ul>
<li><b>1</b> ≤ <b>T</b> ≤ <b>20</b></li>
<li><b>1</b> ≤ <b>N</b> ≤ <b>1*10^9</b></li>
</ul>
<p> </p>
<h3>Example</h3>
<pre><b>Input:</b>
3
1
3
7

<b>Output:</b>
is fibo
is fibo
not fibo
