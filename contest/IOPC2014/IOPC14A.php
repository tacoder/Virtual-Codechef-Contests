<?php require("../../includes/header.php"); ?><h1>Fun with flooring factorial</h1><div class="content">
<p>In a class of <strong>B</strong> students, the teacher wishes to distribute <strong>N!</strong> [ <a href="http://en.wikipedia.org/wiki/Factorial">Factorial</a>(N) ] oranges, such that each one of them gets equal number of oranges.<br /><br />
The teacher, being a lazy person, wants to give away oranges such that she has to take minimum number of them back with her.<br /><br />
Your job is to help the teacher to determine, given <strong>N</strong> and <strong>B</strong>, whether the number of oranges received by each student is even or odd.<br /></p>
<h3>Input Format :</h3>
<p>First line contains <strong>T</strong>, the number of test cases. Next <strong>T</strong> lines follow, each line containing two space separated integers <strong>N</strong> and <strong>B</strong>. <br /><br /></p>
<h3>Output Format :</h3>
<p>The output should contain <strong>T</strong> lines, one for each test case. If the output of a test case is even print "<strong>Even</strong>" (without quotes), otherwise print "<strong>Odd</strong>". <br /><br /></p>
<h3>Constraints :</h3>
<ul>
<li>N &le; 10<sup>5</sup><br /></li>
<li>0 &lt; B ≤ 10<sup>18</sup><br /></li>
<li>0 &lt; T ≤100<br /><br /></li>
<h3>Sample Input :</h3>
<p>2<br /><br />
3 6<br /><br />
2 1<br /><br /></p>
<h3>Sample Output :</h3>
<p>Odd<br /><br />
Even<br /><br /></p>
