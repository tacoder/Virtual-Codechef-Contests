<h1>Sudoku</h1>
<p>Write a program to find a solution to a sudoku puzzle. The input will consist of puzzles with exactly one solution. Or in other words, the Sudoku solved must be a legitimate Sudoku!</p>
<h3>Input</h3>
<p>The input consists of one test case. Each test case will consist of nine lines with nine digits on each line.   There will not be any blanks between the digits.   The digits '1' through '9' will represent a number and the digit '0' will represent a blank square.   The end of input is indicated by the end of the file.</p>
<h3>Output</h3>
<p>For each test case print, in the format shown below, the solved puzzle. There should be no spaces between digits and lines.</p>
