<p>We wish to tile a grid of size Nx2 with rectangles (dominoes) of 2x1 (in either orientation).</p>
<p>Write a program that takes input N and outputs the number of different ways to tile the grid. Output the ans mod 10^9 + 7</p>
<h3>Input</h3>
<p>First line contains T (number of test case)</p>
<p>Next T lines contains N(height of grid) </p>
<h3>Output</h3>
<p>T lines with desired ans mod 1000000007 in each line</p>
<h3>Constraints</h3>
<p>1 &lt;= T &lt;= 1000 1 &lt;= N &lt;= 10^12</p>
<h3>Example</h3>
<p><strong>Input:</strong><br />
