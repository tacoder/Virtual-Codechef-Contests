<?php require("../../includes/header.php"); ?><h1>Cutting Rods</h1><div class="content">

<p>Jimmy has a huge quantity of various steel rods of different lengths that he wants to sell. After a long search he was able to find a buyer. But the buyer has kept a condition that all the pieces of rods must be of same length. The buyer will pay as per the market price per unit length for the rods. Now, for making all rods of same length, Jimmy has to cut the rods which will incur him some cost. The pieces of rods that are left over are thrown away. Help Jimmy to make the cuts such that he can maximize the amount of money he earns. </p>
<h3>Input</h3>
<p>1st line will contain number of test cases</p>
<p>1st line of each test case will contain three integers N, M, C  where N=Number of rods initially, M= Market price of rods per unit length, C = cost incurred for each cut</p>
<p>2nd line of each test case will contain N integers separated by spaces, which are the lengths of each rod. </p>
<h3>Output</h3>
<p>Output the maximum amount of money Jimmy can make</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
2
5 1 2
100 200 100 300 100
5 2 1000
100 200 50 100 100


<b>Output:</b>
794
600

