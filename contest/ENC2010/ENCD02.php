<p>factorizations of a positive integer n is a collection of at least two positive integers, each larger than one, whose product is n. Note that, 2*12 and 12*2 represent the same factorization of 24. In fact, 24 has precisely 6 valid factorizations: 2*2*2*3, 2*2*6, 2*3*4, 2*12, 3*8, and 4*6. so you have to write a program that, given an int n, returns the number of unique factorizations of n.</p>
<h3>Input</h3>
<p>Input will have number of cases. each case having n. n will be between 2 and 2,000,000,000 inclusive. Terminate the input with n=0.</p>
<h3>Output</h3>
<p>for each input, output the number of unique factorizations for that number. </p>
