<p>Our Chef is doing what he is best at, COOKING A BARBECUE for his guests. He has invited all of us, and taking the help of his apprentice to smoke the barbecues. The chef has got BBQ sticks, each can take <b>N</b> fillings, and he presents <b>N</b> distinctly filled sticks in front his guests forming a <b>N*N</b>  matrix</p>
<p>
But here is the problem, he has got only two type of fillings, meat and capsicum, but still wants the <b>N</b>  sticks to look <i>"presentable"</i>, he is very particular about it. As a solution he fills the main diagonal of the <b>N*N</b>  matrix with the same type of filling (either meat or capsicum) forming a <i>"presentable"</i> set</p>
<p>
The Chef's apprentice is a fool, so the Chef asks him to cook <b>M</b> distinctly filled sticks ,so that the Chef is sure that among <b>M</b> there exist <b>N</b> sticks forming a <i>"presentable"</i> set. Your job is to determine smallest possible value of <b>M</b>.</p>
<h3>Input</h3>
<p><b>T</b>, the number of test cases, followed by <b>T</b> lines.
</p><p>Each line containing the positive integer <b>N</b> >= 4</p>
<h3>Output</h3>
<p><b>T</b> lines of output, each line contain the positive integer <b>M</b></p>
<h3>Example</h3>
<pre>
<b>Input:</b>
1
4

<b>Output:</b>
5
