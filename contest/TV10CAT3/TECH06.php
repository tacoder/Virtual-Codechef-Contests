<?php require("../../includes/header.php"); ?><h1>Nails</h1><div class="content">

<p>
One fine day during the summer break Aman and Anish were playing in the tool shed with some nails. Being the little pesky kids that they were, they enjoyed hammering nails into anything they could find. Seeing this their father decided to make their little nail hammering nuisance into more of a learning activity. He proposed a game to the two boys:</p>
<p>"I will give you a nail of some particular length , each of you have to take turns to hammer it into a piece of wood. The person who hammers the nail in last loses the game."</p>
<p>
Now Anish and Aman having similar physical ability could hit the nail inside a piece of would a maximum of 'B' inches and a minimum of 'A' inches inclusive. Anish always starts the game.
</p>
<p>
Given a nail of length 'L' inches and the integers 'A' and 'B', you have to determine the winner of this game assuming both Anish and Aman play optimally.
</p>
<h3>Input</h3>
<p>
First line consists the number of testcases 'T'. The next 'T' lines consist of three space separated integers A, B, L such that 1 &lt; A &lt; B &lt; 1,000,000 and 1 &lt; L &lt; 1000,000,000
</p>
<h3>Output</h3>
<p>
Output either 'Anish' or 'Aman' on a new line indicating the winner of the game for each test case.
</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
2
3 9 12
3 9 13

<b>Output:</b>
Anish
Aman
