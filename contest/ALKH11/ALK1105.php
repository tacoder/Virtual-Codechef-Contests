<p>A function F(n) for a number n is defined as sum of the divisors  of n (including 1 and n) . Given an integer N calculate	G(N) = F(1) + F(2) + F(3) + ..... + F(N)</p>
<h3>Input</h3>
<p>First and only line of input contains an integer N</p>
<h3>Output</h3>
<p>Output a single line containing G(N)</p>
<h3>Constraints</h3>
<p>1 &lt;= N &lt;= 10,000,000</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
3

<b>Output:</b>
8
