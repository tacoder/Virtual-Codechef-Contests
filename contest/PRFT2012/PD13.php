<p>Given a natural number n (1 &lt;= n &lt;= 500000), please output the summation of all its proper divisors.</p>
<p>Definition: A proper divisor of a natural number is the divisor that is strictly less than the number.</p>
<p>e.g. number 20 has 5 proper divisors: 1, 2, 4, 5, 10, and the divisor summation is: 1 + 2 + 4 + 5 + 10 = 22.</p>
<h3>Input</h3>
<p>
An integer stating the number of test cases (equal to about 200000), and that many lines follow, each containing one integer between 1 and 500000 inclusive.</p>
<h3>Output</h3>
<p>
One integer each line: the divisor summation of the integer given respectively.</p>
<h3>Example</h3>
<pre>
<b>Input:</b>

3
2
10
20

<b>Output:</b>

1
8
22

