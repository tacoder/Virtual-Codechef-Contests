<p>You are given an unweighted, undirected graph. Write a program to check if it's a tree topology.</p>
<h3>Input</h3>
<p>
The first line of the input file contains two integers N and M --- number of nodes and number of edges in the graph (0 &lt; N &lt;= 10000, 0 &lt;= M &lt;= 20000). Next M lines contain M edges of that graph --- Each line contains a pair (u, v) means there is an edge between node u and node v (1 &lt;= u,v &lt;= N).</p>
<h3>Output</h3>
<p>
Print YES if the given graph is a tree, otherwise print NO.</p>
