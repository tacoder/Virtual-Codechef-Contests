<p>Ancient Indian Mathematicians like Bhaskaracharya, knew the Pythagoras Theorem even before Pythagoras was born! Though they called it with a different name. They identified the theorem and used them widely as a set of 3 numbers a, b,c such that a*a + b*b = c*c. We'll call this set as 'TriMurthy'. </p>
<p>TriMurthy contains 3 numbers which can be integer, decimal , positive, or even negetive). Any TriMurthy of the form a,b,c represent a right angled triangle which Base = a; Height = b; Hypotenuse = C. We find that cosA = a/c. Hence any TriMurthy represents an angle A.</p>
<p>A 'TriMuthy' is called a 'Vishesh TriMurthy' if its elements are whole numbers and have no common factor. For example 3,4,5 and 24,7,25 are Vishesh TriMurhty, however, 6,8,10 is not a Vishesh TriMurthy (though is is TriMurthy)  because 2 is a common Factor.</p>

<p>Your task is to determine whether a TriMurthy is a Vishesh TriMurthy or Not.</p>
<p>Problem Setter: <a href= " http://bit.ly/VG_MSP_Interview" >Vishal Gupta</a></p>
<h3>Input</h3>
<p>The input contains 3 integers a,b,c in each new line. The number of test cases is not specified. Input Ends with End of File.</p>
<h3>Output</h3>
<p>You have to print YES if a given TriMurthy is a Vishesh TriMurthy otherwise print NO for each set of a,b,c in new line. </p>
<h3>Example</h3>
<pre>
<b>Input:</b>
3 4 5
4 3 5
