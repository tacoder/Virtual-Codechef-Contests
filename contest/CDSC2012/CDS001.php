<?php require("../../includes/header.php"); ?><h1>Lets Cross The Paths</h1><div class="content">

<p>
There is a Cartesian plane. Four points on it represent the houses of Elena Gilbert, Bonnie Bennett, Damon Salvatore and Caroline Forbes. Now, Elena is going to Damon&rsquo;s home and Bonnie to Caroline&rsquo;s. Both walk with same speed and they live in a wind free area. Now, you have to tell if they can meet in the way, anyhow..??</p>
<p>Note: They can meet only if their paths cross each other&rsquo;s.</p>
<h3>Input</h3>
<p>
First line will contain the number of test cases. First line of each test case will contain two numbers x and y, x being x coordinate and y being y coordinate of Bonnie&rsquo;s place. Similarly second, third and fourth line will contain Caroline&rsquo;s, Damon&rsquo;s and Elena&rsquo;s address respectively.</p>
<h3>Output</h3>
<p>
For each test case there will be just one line of output, &ldquo;yes&rdquo; or &ldquo;no&rdquo;.</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
1
0.0 0.0
4.4 4.0
1.0 2.0
2.0 1.0
<b>Output:</b>
yes
