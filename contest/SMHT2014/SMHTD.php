<h3>Problem Statement</h3>
<p>
This  problem is present in SPOJ platform and uploaded by the same author . Lalith is going to have dinner and he has  N dosas in front of him with their prices represented by sequence of integers a1,a2,a3...an. </p>
<p>And he has decided to eat in a different manner . You are free to replace the price of any dosa with any positive integer. </p>
<p>How many prices(integers) must be replaced to make the resulting sequence strictly increasing?</p>
<h3>Input</h3>
<p>The first line of the test case contains an integer N - the number of dosas.</p>
<p>The next line contains N space separated integers where the ith integer is ai , representing thr price of i-th dosa.</p>
<h3>Output</h3>
<p>Output the minimal number of prices(integers) that should be replaced to make the sequence strictly increasing.</p>
<h3>Constraints</h3>
<pre>
0 &lt; N &lt;= 10^6
0 &lt; ai &lt;= 10^9
</pre><h3>Example</h3>
<pre>
Input
6
1 7 10 2 20 22
Output 
1

Input 
5
1 2 2 3 4 
Output 
3
