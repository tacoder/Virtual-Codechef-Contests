<?php require("../../includes/header.php"); ?><h1>Mughal Garden</h1><div class="content">

<p>Vicky has great love for gardening and prime numbers. He wants to create a rectangular garden such that the square of the diagonal of the rectangle is a prime number (the diagonal of course can be any real number)  and its sides are positive integers. You have to help Vicky by telling whether he can create such a garden or not, for the given value of square of diagonal.</p>
<h3>Input</h3>
<p>Number of test cases <b>T(&lt;= 10^6)</b> followed by <b>T</b> lines of numbers having possible values of square of diagonal (a prime number between 1 and 10000000).</p>
<h3>Output</h3>
<p><b>T</b> lines with &ldquo;<b>YES</b>&rdquo; to denote the corresponding value is possible and &ldquo;<b>NO</b>&rdquo; to denote negative response (without quotes).</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
1
2

<b>Output:</b>
YES

<b>Explanation:</b>
2 = 1^2 + 1^2

