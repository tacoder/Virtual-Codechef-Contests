<?php require("../../includes/header.php"); ?><h1>COOKIES</h1><div class="content">

<p>There are n chocolate chips in a dough which is in the form of a plane. Given the locations of the chocolate chips on the dough your task is find out the diameter of the smallest cookie that contains all the chips. You can assume that the cookie is perfectly circular in shape and also the chips have negligible dimensions.</p>
<h3>Input</h3>
<p>First line of input contains the n (&lt;=100) the number of chocolate chips in the dough, followed by n lines of input.<br />
Each line gives the coordinates of one chocolate chip on the plane. Each coordinate is an integer in the range [0,1000].</p>
<h3>Output</h3>
<p>Output consists of a single real number, the diameter of the cookie rounded to two<br />
decimal places.</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
4
1 1
1 0
0 1
0 0


<b>Output:</b>
1.41
