<?php require("../../includes/header.php"); ?><h1>Breaking the Magicians Code</h1><div class="content">
<h2>Problem Statement  </h2>
<p>You are walking on the streets of New York when the famous illusionist David Blaine comes up to you. He is holding an ordered deck of 10 cards numbered 1 to 10 with card 1 at the top and card 10 at the bottom. He gives the pack of cards to you. He asks you to throw away the top card and move the card that is now on the top of the deck to the bottom of the deck and to keep doing this as long as there are at least two cards in the deck. When you are left with the last card in your hand he takes a minute and then says 4. To your amazement when you turn the card over, it is 4. How does he do it?<br />
Write a program to predict the last card when there are n cards in the ordered deck.
</p>
<h3>Input</h3>
<p>Each line of input (except the last) contains a positive number n. The last line contains 0 denoting the end inputs. </p>
<h3>Output</h3>
<p>. For each number from input produce one line of output giving the last remaining card.</p>
<h3>Constraints</h3>
<li><b>1</b> ≤ <b>n</b> ≤ <b>200000</b></li>

<p> </p>
<h3>Example</h3>
<pre><b>Input:</b>
8
15
23
9
0


<b>Output:</b>
8
14
14
2

</pre><p>
