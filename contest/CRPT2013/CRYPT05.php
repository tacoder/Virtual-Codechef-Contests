<h2>Problem Statement </h2>
<p>A Mathematics professor walked into her class. She wanted to test her students’ abilities and hence, gave them a series:<br />
1,1,2,3,5,8….<br />
And asked them to predict the number at a given position.<br />
Write a program to do exactly that. Your task is to take numbers as input (one per line), and print the corresponding number in the inputted position.<br />
Note:  0 Terminates the program.
</p>
<h3>Constraints</h3>
<p>No generated number in excess of 1000 digits will be in the test data, i.e.<br />
Func(25) = 75025 has 5 digits.</p>
