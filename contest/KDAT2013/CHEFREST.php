<p> </p>
<p>Chef opens a new restaurant in the city of Byteland which can be represented in x-y plane. Chef’s restaurant is at point R(0,0). All the (x,y) points in Byteland where x and y both are integers represent a building. One fine day, Chef wants to know the number of buildings which are exactly D unit away from chef restaurant. </p>
<p> </p>
<h3>Input</h3>
<p>- First line contains a number T, number of test cases. Each of the next T line contains a number D</p>
<p> </p>
<h3>Output</h3>
<p>For each test case print the number of buildings which are exactly D unit away from (0,0). </p>
<p> </p>
<h3>Constraints</h3>
<p>T&lt;=100</p>
<p>D&lt;=2000000000</p>
<p> </p>
<h3>Example</h3>
<pre><b>Input:</b>
1
1<br />
<b>Output:</b>
4<br />
</pre><p> </p>
<h3>Explanation</h3>
<p><b>Example case 1.</b>There will be 4 points at a distance of 1 unit from Chefs restaurant, these are (1,0), (-1,0), (0,1), (0,-1)</p>
