<p> </p>
<p>Chef Po is organising a grand party. This time Chef wanted to show off his cooking skills to his guests so he decided to cook N dishes which will cover the dinner table of size A x B such that N=A x B. Chef wanted all his dishes to be different but because of his over confidence K dishes out of N were made similar. Now there is no time left for Chef to prepare new dishes so he is only left with the idea to place dishes in such a way so that no two dishes are placed very close. Two dishes are considered to be placed very close if they are placed side by side in a row or column. Now being a maths lover chef figured out a fact that there can be multiple possible arrangements of dishes which are valid, but he is unable to calculate this number. He asked you to help him in calculating the number of possible valid arrangements of dishes. </p>
<p> </p>
<h3>Input</h3>
<p>First line of input contains a number T, number of test cases. Each of the next T lines contains three numbers A,B and K.</p>
<h3>Output</h3>
<p>For each test case print number of possible valid arrangements.</p>
<h3>Constraints</h3>
<p>0&lt;=A,B,K&lt;=100</p>
<h3>Example</h3>
<pre><b>Input:</b>
2
2 1 1
1 2 2
<b>Output:</b>
2
0
