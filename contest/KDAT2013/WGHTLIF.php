<?php require("../../includes/header.php"); ?><h1>Weightlifting Management</h1><div class="content">

<p>A weightlifter has the regular habit of lifting weights up to a total weight TW assigned by his trainer. He lifts the same weight SW multiple times to complete the limit. When he finds that SW is greater than the remaining weight, he leaves for his home. The same routine is followed every day. The weightlifter is not good with calculations. He needs your help to find the remaining weight and the number of times he lifts weight for each day. Consider that weightlifter goes everyday to the gym without skipping holidays and TW is nonzero only if SW is nonzero.</p>
<h3>Input</h3>
<p>First line of input will contain the number of days D. Each of the test case is described in two lines TW and SW.</p>
<h3>Output</h3>
<p>You have to output the remaining weight and the number of times he lifts weight separated by a space for each day. Also the output for each day should be in a new line.</p>
<h3>Constraints</h3>
<p>0 ≤ TW ≤ 10^100<br />0 ≤ SW ≤ 10^18<br />1 ≤ D ≤ 10^5
</p>
<h3>Example</h3>
<pre><b>Input:</b>
1
132436
23

<b>Output:</b>
2 5758
