<br /><br />
<br /><br />
Using this code, help Code Crunch Members to find out the RD-CODE of their message.<br />
Users will enter 10 digit phone no. Convert these numbers to RD-CODE and also find no of dots used in the coded data.

<h3>Input</h3>
<p>First line T, will have a single integer having number of test cases followed by T lines of input.<br />
<br /><br />
<br /><br />
Second line will have a 10-digit number N where N is a positive integer &lt; 9999999999.
</p>

<h3>Output</h3>
<p>
First line will display the RD-Code for each digit together.<br />
<br /><br />
Second line will display the total number of dots in the RD-CODE.</p>

