<p>
Given three integer  X ,  Y  and Z. Find maximum value of   p   such that Z <sup> p </sup>  divides  factorial of (X*Y).</p>
<h3>Input</h3>

<p>Input will begin with an integer T (at most 100), the number of test cases. T testcases follow, each having three space seprated integer  X, Y, Z  (2 ≤ Z ≤ 10<sup>9</sup> , 2 ≤ Y ≤ 10<sup>9</sup>, 2 ≤ Z ≤ 10<sup>12</sup> ).</p>
<h3>Output</h3>

<p>For each test case, output maximum value of   p.</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
2
2 2 24
5 2 10

<b>Output:</b>
1
2
