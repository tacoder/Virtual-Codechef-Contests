<?php require("../../includes/header.php"); ?><h1>Forever Bhavuk</h1><div class="content">
<p>Bhavuk loves to be in limelight. She tries to get attention of everyone around her. In doing so she sometimes uses her most effective technique called shedding tears. This technique has always worked and everyone around her turns their attention towards her when she uses it.<br /><br /><br />
Bhavuk is quite accustomed to this technique and has become a professional in shedding tears. But these days she is finding it difficult to do so and requires few drops of a catalyst to flood her cheeks. <br /><br /><br />
To grab the attention of everyone around, bhavuk has to shed <b>S</b> tears. Each drop of catalyst <b>C</b> helps her to shed two tears and she has a natural ability to shed <b>N</b> tears without using the catalyst.<br /><br /><br />
Given the number of tears <b>S</b> to be shed, the natural ability of shedding <b>N</b> tears and the volume(in drops) of catalyst <b>C</b> present with bhavuk, find out whether bhavuk is able to become the center of attention.
</p>
<h3>Input</h3>
<p>
First line of input contains <b>T</b> - the number of test cases.<br /><br />
Each test case consists of 3 numbers <b>S N C</b><br /><br />
<b>S</b> - number tears to be shed<br /><br />
<b>N</b> - number of tears that can be shed naturally<br /><br />
<b>C</b> - Volume(in drops) of catalyst
</p>
<h3>Output</h3>
<p>For every test case output "NO" if it is not possible for bhavuk to be the center of attention or "YES" followed by the drops of catalyst used. </p>
<h3>Constraints</h3>
<p>
<b>1</b>&lt;=<b>T</b>&lt;=<b>1000</b><br /><br />
<b>0</b>&lt;=<b>S</b>&lt;=<b>100000</b><br /><br />
<b>0</b>&lt;=<b>N</b>&lt;=<b>100000</b><br /><br />
<b>0</b>&lt;=<b>C</b>&lt;=<b>100000</b>
</p>
