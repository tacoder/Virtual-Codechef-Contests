<br /><br /></p>
<h3>Input</h3>
<p>First line contains the number of test cases T.<br />
<br />Each test case consists of two lines, each line containing one integer. First integer L gives the beginning of the range of values and the second integer U gives the end of range.<br />
<br /><br /></p>
<h3>Output</h3>
<p>One integer in a new line which gives the number of numbers that satisfy the given condition.<br />
<br /><br /></p>
<h3>Constraints</h3>
<p>1 ≤ T ≤ 100<br /><br />
1 ≤ L ≤ U ≤ 10,000<br />
<br /><br /></p>
