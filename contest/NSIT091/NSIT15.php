<?php require("../../includes/header.php"); ?><h1>Not that Digit</h1><div class="content">

<p>
Majin wants to label his N (1 &lt;= N &lt;= 1,000,000) towers. He has lost the labels with the digit L (0 &lt;= L &lt;= 9) written on them. Assume he has sufficient labels to complete the task.</p>
<p>
If the towers are labeled with the smallest set of N positive integers that don't have the digit L, what is the largest number that Majin will label the tower as ?</p>
<h3>Input</h3>
<p>
The first line has an integer 't' (1&lt;=t&lt;=10), the number of test cases. t cases follow.</p>
<p>
Each test case containse two space-separated integers on a single line: N and L</p>
<h3>Output</h3>
<p>
A single integer that is the largest number a tower can have.<br />
<h3>Example</h3>
<pre>
<b>Input:</b>
1
10 0

<b>Output:</b>
11

<b>Explanation</b>

The smallest 10 numbers he can use are: 1, 2, 3, 4, 5, 6, 7, 8, 9, and 11.

