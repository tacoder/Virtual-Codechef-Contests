<?php require("../../includes/header.php"); ?><h1>Prime Relation</h1><div class="content">

<p>
Given a positive integer 'N' , find the number of positive integers less than or equal to N, which are relatively prime to N.</p>
<p>
Two positive integers 'a' and 'b' are called relatively prime if gcd(a,b) = 1 (Where gcd is Greatest Common Divisor).</p>
<h3>Input</h3>
<p>
The first line consists of a number 't' which specifies the number of test cases. 1&lt;=t&lt;=10000. 't' lines follow with a number 'n' on each line. 1&lt;=n&lt;=10^6</p>
<h3>Output</h3>
<p>
For each test case, output the number of positive integers less than or equal to N which are relatively prime to N.</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
5
1
2
4
10
20

<b>Output:</b>
1
1
2
4
8
