<?php require("../../includes/header.php"); ?><h1>Population Estimation</h1><div class="content">

<p>Byteland is a country whose population squares every year. The president of Byteland is aware of the situation and wants to take proper steps to control the population. For this he wants to estimate the population of the country for a given year. As President is weak in Math, He needs your help to solve this problem.</p>
<h3>Input</h3>
<p>First line of Input Contains T, the number of test cases.<br />
For each test case there is a separate line containing two integers,The population of the country in the<br />
year 2000(pi) and the year for which you have to find the population(yi).</p>
<h3>Output</h3>
<p>For each test case output on a separate line the expected population in the year yi.<br />
As this can be very large find its modulo 10^9+7.</p>
<h3>constraints</h3>
<p>1&lt;=T&lt;=300<br />
1&lt;=pi&lt;=10<br />
2000&lt;=yi&lt;=2030</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
2
5 2002
3 2000

<b>Output:</b>
625
3
