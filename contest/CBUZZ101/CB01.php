<?php require("../../includes/header.php"); ?><h1>Binomial Coefficient</h1><div class="content">

<p>This is a very simple problem. Given the value of N and K, you need to tell us the value of the binomial coefficient C(N,K). You may rest assured that K &lt;= N and the maximum value of N is 1,000,000,000,000,000. Since the value may be very large, you need to compute the result modulo 1009.</p>
<h3>Input</h3>
<p>The first line of the input contains the number of test cases T, at most 1000. Each of the next T lines consists of two space separated integers N and K, where 0 &lt;= K &lt;= N and 1 &lt;= N &lt;= 1,000,000,000,000,000.</p>
<h3>Output</h3>
<p>For each test case, print on a new line, the value of the binomial coefficient C(N,K) modulo 1009.</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
3
3 1
5 2
10 3
<b>Output:</b>
3
10
120
