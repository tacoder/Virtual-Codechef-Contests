<?php require("../../includes/header.php"); ?><h1>String encryption</h1><div class="content">

<p> A simple string contains a large repetition of letters within it. This problem is related to string handling and manipulation.  An original message is sent from planet Earth to planet Cybertron in form of a string. However, the letter position and string size is not important. The number of time each letter has occurred in the string is important. So the original string which is sent to Cybertron is encrypted in the new string which comprises the letters followed by each time it has occurred in the original string. Eg- original message is- abcdabf. Then the encrypted string is- a2b2c1d1f1</p>
<h3>Input</h3>
<p>The input consists of a single line string without any space or numeric or special characters.</p>
<h3>Output</h3>
<p>It will consist of in the encrypted string which comprises the letters followed by each time it has occurred in the original string in order.</p>
