<p> </p>
<p>Stepping Stones is a very famous game in <b>Jamia Millia Islamia.</b><br />
Given <b>N</b> number of milestones where each number written on the milestone represents the maximum number of steps that can be made from it.<br />
Lebron James wants to reach the last milestone or beyond in minimum no of steps if he starts from the first milestone.<br />
Help lebron!!!!.</p>
<p> </p>
<h3>Input</h3>
<p>The first line contains <b>T</b> representing the number of test cases.<br />
Every test case contains the following:<br />
The first line is N the number of milestones.<br />
The next line conatins number written on each milestone A[1....N].
 </p>
<h3>Output</h3>
<p>Give the minimum no of jumps required by lebron to reach the last milestone. </p>
<h3>Constraints</h3>
<ul>
<li><b>1</b> ≤ <b>T</b> ≤ <b>50</b></li>
<li>1&lt;= N &lt;= 1000</li>
<li>1&lt;= A[i] &lt;= 10000</li>
</ul>
<p> </p>
<h3>Example</h3>
<pre><b>Input:</b>
1
5
1 2 3 4 5

<b>Output:</b>
3
</pre><p> </p>
