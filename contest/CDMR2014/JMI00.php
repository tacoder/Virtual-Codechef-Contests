<?php require("../../includes/header.php"); ?><h1>DORA-CAKES</h1><div class="content">
<p> </p>
<p> Doraemon likes to fly with his bamboo-copter.But the major problem is that he can fly only at the same height or fly higher but he can't come down.<br />
He has a lot of buildings in front of him say <b>N</b> and on the top of every building there is a DORA-CAKE. Knowing his potential of flying he wants to eat maximum DORA-CAKES as he is very fond of them. So given the number of buildings <b>N</b> and height of each building A[i], your duty is to find the maximum number of DORA-CAKES he can eat.
</p>
<p> </p>
<h3>Input</h3>
<p>The first line contains T the number of test cases.<br />
For every test case take the input as <b>N</b> and the heights of buildings of every building A[1],A[2],...A[N] in the next line.
</p>
<h3>Output</h3>
<p>For each test case output the maximum DORA-CAKES Doraemon can eat.</p>
<h3>Constraints</h3>

<ul>
<li><b></b>1 ≤ <b>T</b> ≤ 100<b></b></li>
<li>1&lt;=N&lt;=1000</li>
<li>0&lt;=A[i]&lt;=1000000</li>
</ul>
<p> </p>
<h3>Example</h3>
<pre><b>Input:</b>
2
3
1 2 1
9
2 5 3 7 11 8 10 13 6

<b>Output:</b>
2
6 
