<p>Chef Polo is experimenting with Prime Numbers. He can quickly tell whether a number is prime or not. His friend Palin knows about it, so he twists the question.<br/> Palin challenges his friend to check whether a number <b>P</b> is <b>"almost prime"</b> or not.<br/><br />
The number <b>P</b> is said to be <b>almost prime</b> if it has exactly 3 distinct divisors.<br/><br />
A number <b>d</b> is said to be divisor of <b>P</b> if <b>P</b> is divisible by <b>d</b>.<br />
<br/><br />
Chef Polo is quite intelligent but currently he is busy with preparing delicious dishes. So he wants your help.<br />
</br/></br/></br/></br/></p>
<h3>Input</h3>
<p>First line Contains an integer <b>T</b>, the no of test cases.<br/><br />
Each of next <b>T</b> lines contain a single integer <b>P</b>, the number Palin asks to check.<br />
</br/></p>
<h3>Output</h3>
<p>
For each test case, output a  string <b>"YES"</b> if the number <b>P</b> is almost prime. Otherwise output <b>"NO"</b>
</p>
<h3>Constraints</h3>
<ul>
<li><b>1</b> ≤ <b>T</b> ≤ <b>10^5</b></li>
<li><b>1</b> ≤ <b>P</b> ≤ <b>10^12</b></li>
</ul>
<h3>Example</h3>
<pre><b>Input:</b>
