<p>A balanced prime is one which is the average of the previous and the next prime number. Your task in this problem is to write a program that can calculate the nth balanced prime</p>
<h3>Input</h3>
<p>The first line of input contains t, the number of test cases. This is followed by t lines each containing a number n.</p>
<h3>Output</h3>
<p>For each test case output the nth balanced prime on a new line.</p>
