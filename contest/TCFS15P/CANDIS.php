<p> Nehal is a kindergarten teacher. She has a lot of candies and wants to give some of them to her students. All the <b>N</b> students sit in a line, and each of them must be given at least one candy. She wants to distribute the candies in such a way that the product of the number of candies any two adjacent kids have, is not greater than <b>M</b>. </p>
