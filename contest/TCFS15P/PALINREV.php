<p> </p>
<h3>Problem description.</h3>
<p>
Puck, the trickster, has again started troubling people in your city. The people have turned on to you for getting rid of Puck.  Puck presents to you a string consisting of lowercase character's from English alphabets. He wants you to <b>reverse a substring </b> from the string such that the string becomes <b>Palindrome</b>. A Palindrome is a string which equals its reverse. The hope of  people are on you so you have to solve the riddle.
</p>
<p>
You have to tell if some Substring exists which you would reverse to convert the string into palindrome.
</p>
<h3>Input</h3>
<p>Input consists of single line denoting the string Puck gives.</p>
<h3>Output</h3>
<p>Output "<b>Yes</b>" or "<b>No</b>"  ( quotes for clarity )based on whether required substring exists.</p>
<p> </p>
<h3>Constraints</h3>
<ul>
<li>String contains only lowercase characters <b>{ a, b, .. z }</b></li>
<li>1 &lt;= length of string &lt;= 5 * 10 ^ 4</li>
</ul>
<p> </p>
<h3>Example</h3>
<pre><b>Input:</b>
tasdasdt

<b>Output:</b>
Yes
</pre><p> </p>
<h3>Explanation</h3>
<p><b>Example case 1.</b><br />
Reversing substring (4, 6) makes string tasddsat , which is palindrome.
