<p>
Shridhar wants to generate some prime numbers for his cryptosystem. Help him!<br />
Your task is to generate all prime numbers between two given numbers.
</p>
<h3>Input</h3>
<p>
The first line contains t, the number of test cases (less then or equal to 10). </p>
<p>Followed by t lines which contain two numbers m and n (1 &lt;= m &lt;= n &lt;= 1000000000, n-m&lt;=100000) separated by a space.
</p>
<h3>Output</h3>
<p>For every test case print all prime numbers p such that m &lt;= p &lt;= n,<br />
one number per line.  Separate the answers for each test case by an empty line.</p>
<h3>Example</h3>
<pre><b>Input:</b>
2
1 10
3 5

<b>Output:</b>
