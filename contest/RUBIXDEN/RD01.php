<?php require("../../includes/header.php"); ?><h1>Sum of Coefficients</h1><div class="content">

<p>
The Problem is pretty simple and straight forward.<br />
Calculate the sum of coefficients of (x+y)^n
</p>
<h3>Input</h3>
<p>
First line will contain the number of Test Cases T( 0 &lt; T &lt; 10^5 ). Further T line will contains one number n. ( 0 &lt; n &lt; 10^7 ).
</p>
<h3>Output</h3>
<p>
For each Test Case print single line having the sum of coefficients. If result exceed 1000000007 take mod with the result.
</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
5
10
20
30
40
50

<b>Output:</b>
1024
1048576
73741817
511620083
898961331
