<h3>Problem description.</h3>

<p>Brainwave is a hardware designing competition, which deals with the implementation hardware knowledge in an ingenious way. The problem statement for participant was to make the simple hardware for addition. Now you have to make a program which adds two strings, here 'A'=0, 'B'=1, 'C'=2, and so on.</p>
<h3>Input</h3>
<p>There is a single positive integer T on the first line of input. It stands for the number of cases to follow.  Each case consists of two strings A and B. Character in strings lies A-Z only.</p>
<h3>Output</h3>
<p>Output consists of T lines, each line has a string representing addition of given strings.Output's string length will be atleast max (length of A,length of B).</p>
<h3>Constraints</h3>
<p>1&lt;=T&lt;=100<br /><br />
1&lt;=length of strings&lt;=100000<br />
</p>
