<?php require("../../includes/header.php"); ?><h1>Counting Bits</h1><div class="content">
<p>
Given <b>N</b>, if we write all numbers from <b>1</b> to <b>N</b> (both inclusive) in binary what is<br />
the count of 1s I have written.<br />
For example, if N=3,<br />
I will write down:<br />
1<br />
10<br />
11</p>
<p>Therefore, a total of 4 ones.
  </p>
<h3>Input Format:</h3>
<p>
First line contains, <b>T</b>, the number of testcases. Each testcase consists of one integer per line denoting <b>N</b>.
  </p>
<h3>Output Format:</h3>
<p>
Print the required answer.
  </p>
<h3>Constraints:</h3>
<p><b><br />
  1 &le; T &le; 1000<br />
1 &le; N &le; 1000</b>
  </p>
<h3>Sample Input:</h3>
<pre>
1
3</pre><h3>Sample Output:</h3>
<pre>
4</pre><p><br />
