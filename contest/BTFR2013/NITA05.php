<p>Class Square in the capital city of India has a rectangular shape with the size n × m meters. On the occasion of the city's anniversary, a decision was taken to pave the Square with square granite flagstones. Each flagstone is of the size a × a.</p>
<p>What is the least number of flagstones needed to pave the Square? It's allowed to cover the surface larger than the Theatre Square, but the Square has to be covered. It's not allowed to break the flagstones. The sides of flagstones should be parallel to the sides of the Square.
</p>
<p> </p>
<h3>Input</h3>
<p>The input contains three positive integer numbers in the first line: n,  m and a (1 ≤  n, m, a ≤ 10^9).</p>
<h3>Output</h3>
<p>Write the needed number of flagstones.</p>
<p> </p>
<h3>Example</h3>
<pre><b>Input:</b>
6 6 4

<b>Output:</b>
4
