<p>
The "He-Man" Gymnasium, Jabalpur wants to organise a weight-lifting competition.<br />
The weight discs will be placed on the ends of a single long weight lifting bar.<br />
The competitor lifts the weight bar by holding it in the middle ( like on the practice problems page of Codechef ! ).<br />
The problem is, the Gymnasium does not have weight discs of  equal weight in equal numbers.<br />
Manager Monnu wants to determine the maximum weight that can be used in the competition. Obviously the weight has to be equal on both sides of the bar!  </p>
<p>Given a list of the weights of the discs available in the gymnasium, find the maximum weight that a participant may have to lift. </p>
<h3>Input</h3>

<p>
The first line gives <i>t</i> the number of test cases (<i>t</i> ≤ 100).<br />
Then <i>t</i> test cases follow.</p>
<p>For each test Case:<br />
The first line gives <i>n</i>, the number of weight discs in the gym. (2 ≤ n ≤ 60)<br />
The next line contains <i>n</i> integers which give the weights of the <i>n</i> discs. The total weight of the weight discs in the gymnasium is less than 600000 </p>
<h3>Output</h3>

<p>
For each test case output a single line, giving the maximum amount of weights the can be put on both sides of the bar</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
1
5         
10 7 8 2 4 

<b>Output:</b>
12
