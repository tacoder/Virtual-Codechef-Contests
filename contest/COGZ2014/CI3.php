<p>Problem description:</p>
<p>Given the three sides of a N triangles a,b and c,display the area of the triangle that has minimum area,maximum area,average of the given N triangles area and median of areas of given traingles(round it off to three decimal points if any of output to the given case is a fraction having decimal points greater than 3 digits).   </p>
<h3>Input</h3>
<p>The first line of the input contains an integer N representing the number of triangles.The following N lines contain three sides of a each triangle a,b,c separated by spaces.</p>
<h3>Output</h3>
<ul>
<li>
Output the area of the triangle that has minimum area,maximum area,average of the given N triangles area and median of areas of given traingles(round it off to three decimal points if any of output to the given case is a fraction having decimal points greater than 3 digits) in separate line in the format given in the output.
</li>
</ul>
<p> </p>
<h3>Constraints</h3>
<ul>
<li><b>1</b> ≤ <b>N</b> ≤ <b>100</b></li>
<li><b>1</b> ≤ <b>a,b,c</b> ≤ <b>100</b></li>
</ul>
<p> </p>
