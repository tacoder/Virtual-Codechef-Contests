<h3>Problem description</h3>
</p>
<p>Given two integers A and B that are not necessarily in base-10, find the smallest possible A + B in base-10.</p>
<p>
For example,<br />
A = 213, possibly base-4 (39 in base-10)<br />
B = 4721, possibly base-8 (2513 in base-10)<br />
A + B = 39 + 2513 = 2552 </p>
<h3>Input</h3>
<p>
First line of the input contains a positive integer T (1 &lt;= T &lt;= 100), the number of cases. Each case contains<br />
two positive integers A and B. A and B will contain at most 5 digits, each digit will be between 0 and 9,<br />
inclusive, and no leading zeroes.</p>
<h3>Output</h3>
<p>
For each case, output an integer in base-10 denoting the smallest possible A + B.</p>
<h3>Example</h3>
<pre><b>Input:</b>
3
213 4721
1001 90
638 241

<b>Output:</b>
2552
99
