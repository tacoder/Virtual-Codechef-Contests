<p>
We all have played a game called FLAMES during our childhood. In this we take input as two names, eliminate the similar alphabets in both the words and count the remaining number of alphabets and calculate the corresponding relationship according to FLAMES(Friend, Love, Affection, Marriage, Enemy or Sister)</p>
<h3>Input</h3>

<p>Input description...<br />
Name of you and your partner</p>
<h3>Output</h3>

<p>Output description...<br />
Count the number of remaining dissimilar alphabets and according to flame find the corresponding relationship<br />
F- FRIEND<br />
L- LOVE<br />
A-AFFECTION<br />
M- MARRIAGE<br />
E - ENEMY<br />
S- SISTER</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
RAHUL
PREETI

<b>Output:</b>
E- ENEMY
</pre>
