<p>To find the zeroes in a factorial of a number. In this problem, we have to find the number of zeroes following a non negative number in a factorial of a number. Suppose the number entered is 6 and we know factorial of 6 is 720 thus there is only one zero after a non- negative number, so the output is 1.</p>
<h3>Input</h3>

<p>The first number N indicates the number of test cases to be followed.It stands for number of numbers to be followed. Then there are N lines each containing exactly one positive integer for which the solution to be found out.</p>
<h3>Output</h3>

<p>For every number, output a single line containing the single non negative integer.</p>
<h3>Example<br />
</h3>
<pre>
<b>Input:</b>Sample Input

6
3
18


<b>Output:</b>
1
0
5
</pre>
