<?php require("../../includes/header.php"); ?><h1>Prime or not</h1><div class="content">

<p>
Alice is very fond of prime numbers. Given a number she wants to determine whether it is prime or not. If a number is prime she wants to know that which prime number is it.<br />
e.g. 2 is 1st prime number,3 is 2nd and so on.</p>
<h3>Input</h3>
<p>First line of input contains number of test cases t. t lines follow. Each line contains a positive integer(&lt;10^9). </p>
<h3>Output</h3>
<p>For each test case output a single integer. If the number is not prime output -1 otherwise output a integer say n such that the number is nth prime number.</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
3
2
3
4

<b>Output:</b>
1
2
-1
