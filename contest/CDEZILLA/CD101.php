<p>Three dinos go on a camping trip. There they decide to play a game. They mark distances of 1 km each on the ground up to 100 km i.e. 1, 2, 3...100. They can sit only on these 100 spots, with each one at a different spot. In one move, one of the outer dinos can hop into a spot between the other two.<br />
Help them maximise their duration of play. </p>
<h3>Input</h3>
<p>Three numbers(integers), l, m and n (0 &lt; l &lt; m &lt; n &lt; 100), the starting spots of the dinosaurs.</p>
<h3>Output</h3>
<p>Output the largest number of jumps the dinosaurs can make in all.</p>
<h3>Example</h3>
<pre>
<b>Input</b>
4 5 7
<b>Output</b>
1
<b>Input</b>
5 6 11
<b>Output</b>
