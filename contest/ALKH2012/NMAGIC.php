<p>
Alice is a very brilliant student. He considers '4' and '7' as Magic numbers. The numbers containing only magic numbers are also magical. Given a magic number <b>N</b> ,he wants to know what could be the next magical number greater than the given number.</p>
<h3>Input</h3>
<p>
First line of input contains number of test cases <b>T</b>. For each test case, there is exits only one line containing a magic number <b>N</b>. </p>
<h3>Output</h3>
<p>
For each test case,  output a single line containing the next greater magical number.</p>
<h3>Constraints</h3>
<pre>
1&lt;=<b>T</b>&lt;=1000
4&lt;= <b>N</b>&lt;=10^100
</pre><h3>Example</h3>
<pre>
<b>Input:</b>
