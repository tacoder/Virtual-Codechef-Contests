<?php require("../../includes/header.php"); ?><h1>A Simple Sum</h1><div class="content">

<p>
Given three integers <b>x</b>, <b>m</b> and <b>n</b>. Evaluate (1 + <b>x</b> + <b>x</b><sup> 2</sup> + .. + <b>x</b><sup> <b>m</b></sup>)  (mod <b>n</b>)
</p>
<h3>Input</h3>
<p>
First line of the input contains a single integer T representing number of test cases that follow.
</p>
<p>
For next T lines, each line contains three space separated integers <b>x</b>, <b>m</b> and <b>n</b>.
</p>
<h3>Output</h3>
<p>
Output exactly T lines each containing the answer to the corresponding test case.
</p>
<h3>Constraints</h3>
<ul>
<li>1 ≤ <b>T</b> ≤ 1000 </li>
<li>1 ≤ <b>x</b>, <b>n</b>, <b>m</b> ≤ 10^16</li>
</ul>
<h3>Example</h3>
<pre><b>Input:</b>
1
3 2 5

<b>Output:</b>
3
