<?php require("../../includes/header.php"); ?><h1>BRIDGING THE GAP</h1><div class="content">

<p>Our lovely chef once won a free holiday to the amazing Maldives. But after reaching there he found out that the island which was next to him was better than his current, but unfortunately there was no bridge through which he can go to that island. Since he was very creative he found out a way to create a bridge between  the two islands </p>
<p>There were infinite wooden plank of mass M and length L on the chef's island. Now you have to calculate at least how many planks he will require to create a bridge between two island if the distance between those two island is W.</p>
<h3>Input</h3>
<p>The first line of the input contains an integer T denoting the number of test cases, for each test case input L i.e. length of planks and W distance between islands.</p>
<h3>Output</h3>
<p>For each test case, output a single line with minimum number of planks required to make bridge.</p>
<h3>Constraints</h3>
<ul>
<li><b>1</b> ≤ <b>T</b> ≤ <b>100</b></li>
<li><b>1</b> ≤ <b>L</b> ≤ <b>60</b></li>
<li><b>1</b> ≤ <b>W</b> ≤ <b>50</b></li>
</ul>
<p> </p>
<h3>Example</h3>
<pre><b>Input:</b>
2
4 5
10 9
<b>Output:</b>
7
1
</pre><p> </p>
<h3>Explanation</h3>
<p><b>Case 1 : </b> for L = 4m and W = 5m , bridge can be constructed using minimum of 7 planks.</p>
<p><b>Case 2 : </b> for L = 10m and W = 9m , bridge can be constructed using only single planks.</p>
