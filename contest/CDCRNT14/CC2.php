<h3>Problem description.</h3>
<p></p></b></p>
<p> Rihanna wonders why the starting numbers of Fibonacci series are always 0 and 1. She didn’t like that and thus want to change the starting numbers.</p>
<p>
So given the 1st and 2nd numbers of series as <b>a</b> and <b>b</b> respectively. Find out the <b>‘r’</b> th number in the series for given <b>r</b>.</p>
<h3>Input</h3>
<p>First line has integer <b>T</b>, the number of test cases<br /><br />
Next <b>T</b> lines contain <b>T</b> test cases<br /><br />
each line contains integers <b> a ,b, and r</b> <br />
</p>
<h3>Output</h3>
<p>For each test case output the answer modulo 10^9 +7</p>
<h3>Constraints</h3>
<p>1&lt;=T&lt;=50000<br /><br />
0&lt;=a,b&lt;=1000000<br /><br />
1&lt;=r&lt;=1000000<br /><br />
 </p>
<h3>Example</h3>
<pre><b>Input:</b>
<p>
2
0 1 3
2 3 1
</p>

<b>Output:</b>
<p>
1
2
