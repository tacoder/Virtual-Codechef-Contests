<?php require("../../includes/header.php"); ?><h1>Rihanna and Chris Brown</h1><div class="content">
<h3>Problem description.</h3>
<p>Chris brown and Rihanna are in a <b>nxn</b> grid(1 indexed). Chris brown want to reach Rihanna in minimum number of moves but the thing is he can move only diagonally and whatever may be the number of steps he takes it is considered as only one move unless he takes a turn. So in one move he can take as many steps as he wants but should not take a turn, if he takes a turn it is considered as another move. </p>
<h3>Input</h3>
<p>First line has integer T, the number of test cases<br /><br />
In each test case first line contains integer <b>n</b><br /><br />
Next line contains 4 integers<b> x1, y1, x2, y2</b>  first two are the co-ordinates of chris brown and the next two are co-ordinates of rihanna.<br />
 </p>
<h3>Output</h3>
<p> Print the minimum number of moves.<br /><br />
If he can’t reach print  <b>-1</b>
</p>
<h3>Constraints</h3>
<p>1&lt;=T&lt;=50000<br /><br />
1&lt;=n&lt;= 2^64 - 1  <br /><br />
1&lt;=x1,y1,x2,y2&lt;=n<br />
 </p>
<h3>Example</h3>
<pre><b>Input:</b>
2
4
1 1 3 3 
4
1 1 1 3

<b>Output:</b>
1
2
