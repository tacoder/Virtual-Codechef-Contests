<p>Given a undirected graph find the number of connected components.</p>
<h3>Input</h3>
<p>First line of the input is 't'- number of test case.<br/> Followed by N, the number of vertices  (Numbered 0 to N-1).<br/> Followed by 'e' number of edges.<br/> Followed by description of  'e' edges in the form 'a b'  I.e. an edge exist between vertex a and b.<br/><br />
Constraints:<br/><br />
T=10<br/><br />
2 &lt;= N &lt;= 100000<br/><br />
0 &lt;= e &lt;= N/2<br/></br/></br/></br/></br/></br/></br/></br/></br/></p>
<h3>Output</h3>
<p>One line per test case, number of connected components in the graph.</p>
<h3>Example</h3>
<pre>
<b>Input:</b>
2
4
2
