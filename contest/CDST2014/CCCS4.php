<p>Bob wants to change the tiles of his room’s flooring  which has dimensions <b>m</b> x <b>m</b> sq. m. Dimensions of the tiles used are <b>n</b> x <b>n</b> sq. m. All the tiles are red except diagonal tiles which are black. Now Bob wants to replace some red tiles by black tiles to form an alternate black and red pattern.<br />
