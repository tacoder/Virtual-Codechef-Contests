<h3>Problem description.</h3>
<p>Masadla is very passionate about his business. He has been working on it for past many years and wants to make it  big. For this purpose he will be meeting an investor in the coming week. In the meeting he would have to show a graphical representation of the profit his business has been making .
</p>
<p>
He knows that to impress the investor,  he must have a good non-dropping profit graph for his company. But unfortunately this wasn’t the case,  so he decides to be clever and drop some numbers and make the graph non - dropping. But there is a catch -  he can’t drop a lot of numbers as investor would then get to know what he has done .Can you help him figure out, how many such numbers at best he can retain which would satisfy the criteria he needs for plotting the graph?</p>
